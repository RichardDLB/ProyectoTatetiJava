    public void presionar(int casilla){
        if (lbs[casilla-1].getText().equals("")) 
        {          
            lbs[casilla-1].setText(turno);
            cambiarturno();
            comprobarganador();
        }
    }
    
    public void reiniciar(){
        for (int i = 0; i < lbs.length; i++) 
        {
            lbs[i].setText("");
            lbs[i].setBackground(Color.white);
        }
    }
    
    public void reiniciar2(){
        String [] boton = {"Si", "No"};
        
        int eleccion = JOptionPane.showOptionDialog(this, "Volver a jugar?", texto,
                0, 0, null, boton, EXIT_ON_CLOSE);
        
        if (eleccion == JOptionPane.NO_OPTION) 
        {
            System.exit(0);
        }
        
        if (eleccion == JOptionPane.YES_OPTION) 
        {
            Nombres n = new Nombres();
            n.setVisible(true);
            this.setVisible(false);
        }
    }
    
    public void comprobarganador(){
        
        for(int i= 0; i < vs.length; i++)
        {
            if (lbs[vs[i][0]-1].getText().equals("X")&&
                   lbs[vs[i][1]-1].getText().equals("X")&&
                   lbs[vs[i][2]-1].getText().equals("X"))
            {
               lbs[vs[i][0]-1].setBackground(Color.yellow);
               lbs[vs[i][1]-1].setBackground(Color.yellow);
               lbs[vs[i][2]-1].setBackground(Color.yellow);
               
               JOptionPane.showMessageDialog(this, "Gano " + texto);
               lblV1.setText(Integer.toString(Integer.parseInt(lblV1.getText())+1));
               
               reiniciar();
               
               if (Integer.parseInt(lblV1.getText()) == 3) 
               {
                   JOptionPane.showMessageDialog(this, texto + " gano el juego");
                   reiniciar2();
               }
               
            }
            if (lbs[vs[i][0]-1].getText().equals("O")&&
                   lbs[vs[i][1]-1].getText().equals("O")&&
                   lbs[vs[i][2]-1].getText().equals("O"))
            {
                
                lbs[vs[i][0]-1].setBackground(Color.yellow);
                lbs[vs[i][1]-1].setBackground(Color.yellow);
                lbs[vs[i][2]-1].setBackground(Color.yellow);
                
               JOptionPane.showMessageDialog(this, "Gano " + texto1);
               lblV2.setText(Integer.toString(Integer.parseInt(lblV2.getText())+1));
               
               reiniciar();
               
               if (Integer.parseInt(lblV2.getText()) == 3) 
               {
                   JOptionPane.showMessageDialog(this, texto1 + " gano el juego");
                   reiniciar2();
               }
               
            }
            else
            {
                empate();
            }
        }
    }
    
    public void cambiarturno(){
            if (turno.equals("X")) 
            {
                turno="O";            
            }
            else
            {
                turno="X";
            }
    }
    
    public void empate(){
        if(lbl1.getText() != "" && lbl2.getText() != "" && lbl3.getText() != "" && lbl4.getText() != "" 
          && lbl5.getText() != "" && lbl6.getText() != "" && lbl7.getText() != "" && lbl8.getText() != "" 
          && lbl9.getText() != "")
        {
            JOptionPane.showMessageDialog(this, "Empate");
            reiniciar();        
        }
    }
    
